package _00_BasicsOfJava;

public class incrementDrecement {
    public static void main(String[] args) {
        int x=5;
        int y = 6;
        System.out.println(x);
        x++;
        System.out.println(x);

        System.out.println(y);
        y--;
        System.out.println(y);
    }
}
