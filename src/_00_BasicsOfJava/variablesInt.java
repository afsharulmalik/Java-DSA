package _00_BasicsOfJava;

public class variablesInt {
    public static void main(String[] args) {
        int x;
        x =500;                     // this is initialization
        System.out.println(x);
        x=69;                    // this is updation
        System.out.println(x);
        System.out.println(x+1);    // this is increment
    }
}
